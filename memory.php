        <h3>Memory</h3>       
        <p>Computer’s memory can be classified into two types; primary memory and secondary memory</p>
        <p><b><h4>a. Primary Memory can be further classified as RAM and ROM.</h4></b></p>
        <p><b>RAM or Random Access Memory</b> is the unit in a computer system. It is the place in a computer where the operating system, application programs and the data in current use are kept temporarily so that they
        can be accessed by the computer’s processor. It is said to be ‘volatile’ since its contents are accessible only as long as the computer is on. The contents of RAM are no more available once the computer is turned off.</p>        
       
        <p><b>ROM or Read Only Memory</b> is a special type of memory which can only be read and contents of which are not lost even when the computer is switched off. It typically
        contains manufacturer’s instructions. Among other things, ROM also stores an initial program called the ‘bootstrap loader’ whose function is to start the
        operation of computer system once the power is turned on.</p>
        <p><b><h4>b. Secondary Memory</h4></b></p>
        <p><b>RAM</b> is volatile memory having a limited storage capacity. Secondary/auxiliary memory is storage other than the RAM. These include devices that are peripheral and are connected and controlled by the computer to enable
        permanent storage of programs and data. Secondary storage devices are of two types; magnetic and optical. Magnetic devices include hard disks and optical storage devices are CDs, DVDs, Pen drive, Zip drive etc.</p>
        <p><i class="fa fa-hand-point-right"> <b>Hard Disk</b></i></p>
        
        <div class="img">
            <img src="images/hard disk.jpg" alt="hard disk">
            <p> Figure: Hard Disk</p>
        </div>
        <p><b>Hard disks:</b> are made up of rigid material and are usually a stack of metal disks sealed in a box. The hard disk and the hard disk drive exist together as a unit and is a
        permanent part of the computer where data and programs are saved. These disks have storage capacities ranging from 1GB to 80 GB and more. Hard disks are rewritable.</p>
        <p><i class="fa fa-hand-point-right"> <b>Compact Disk</b></i></p>
        <p><b>Compact Disk (CD)</b> is portable disk having data storage capacity between 650-700 MB. It can hold large amount of information such as music, full-motion videos, and text etc. CDs can be either read only or read write type.</p>
        <p><i class="fa fa-hand-point-right"> <b>Digital Video Disk</b></i></p>
        <p><b>Digital Video Disk (DVD)</b> is similar to a CD but has larger storage capacity and enormous clarity. Depending upon the disk type it can store several Gigabytes of data. DVDs
        are primarily used to store music or movies and can be played back on your television or the computer too. These are not rewritable.</p>
