        <h3>History of Computer </h3>
        <p>Since the creation of man, a significant amount of human activities has been ascribed to organizing and processing information so that it could be more easily presented for easy comprehension. Many devices have 
            been used in the past before the advert of computer. It is then necessary to vividly look into their evolution.</p> <p>Early computing machines:</p>
          <p>
         <p><b> 1. Abacus (-2500BC):</b> This is a hand- held device made of beads stung on rods in a frame. The rods correspond to positions of the digits while the beads correspond to the digits.
        <p><b>2. Napiers Bone (2500BC):</b> This was invented by John Napiers (1550 - 1617). This consists of small rods with appropriate markings on them. It is a mechanical aid to computation that consists of nine such rods (called bones) with one for each digit 1 through 9. He also invented logarithms which made possible to do division and multiplication by performing addition and subtraction.</p>
        <p><b>3. Slide Rule (1600AD) by William Oughtred (1575 - 660):</b> He invented it in 1622 but announced it in 1632 this consist of rules on which markings represent logarithms of numbers and also permits calculation involving exponents, trigonometric functions, etc.
        <p><b>4. Pascal mechanical calculator (1600) or Numerical wheel calculator:-</b>Blaise Pascal (1623 -1664) in 1642 invented the first adding machine called Pascaline. The brass rectangular box used eight moveable dials to add and sum up of eight figures long using base 10. It can perform all the four arithmetic operation with previous unheard speed.
        <p><b>5. Leibnitz mechanical multiplier (1600):</b> In 1694 Gottfried Wilhem Von Leibnitz (1646 -1716) improved upon the pascaline by creating a machine that can also multiply using a system of dials and gear. 6 Colmars Calculator (1820) by Charles Xavier Thomas de Colmar: This presented a more practical approach to computing.
        <p></b>7. Punched-Card machine (Jacquards loom) (1801):</b> Joseph Marie Jacquard.
        <p><b>8. Mechanical computer:</b> Charles Gabbage (1792-1871) Father of the computer. Difference engine powered by steam and large as locomotive the machine has a stored program and could perform calculations and print the result automatically. We also have Analytical engine credited to him. 
        <p><b>9. Hermann Hollerith (1860-1929)</b><br>
        <i class="fa fa-caret-right"> Helloriths system punch-card reader machine:-for counting census result in 1890 in US.</i>
        <i class="fa fa-caret-right"> Formed tabulating machine company in 1896(TMC)</i>
        <i class="fa fa-caret-right">TMC was renamed to International Business Machines Corporation (IBM) in 1924 after series of mergers.</i>
        <p>In summary, the history of computing began with an analog machine. In 1623 German scientist Wilhelm Schikard invented a machine that could add, and with the aid of logarithm tables, multiply and divide. Since then the development has pass through a lot of stages such as the invention of punched cards to program patterns to create woven fabrics by Joseph-Marie Jacquard a French inventor in 19th century.</p>
        
