        <h3>COMPUTER SOFTWARE</h3>
        <p>Software is the set of instruction that tells the computer what to do and when to do it. The computer uses this instruction to manipulate data, and enhance the proper functioning of the hardware components. It is
        designed to exploit and provide the potential capabilities of the hardware to the user. It converts data into information and allows users to use the computer in different ways.</p>
        <p>These programs are usually stored and transferred via the computers hardware to and from the CPU. Software also governs how the hardware is utilized; for example, how information is retrieved from a storage
        device. The interaction between the input and output devices is controlled by software called the Basic Input Output System (BIOS) Software. Software as a whole can be divided into a number of categories based on the
        types of work done by programs.</p> <p>The two primary software categories are system software, and application software.</p>
        <h4>System Software</h4>
        <p>System software consists of a group of programs that control the operations of a computer equipment including functions like managing memory, managing peripherals, loading,
        storing, and is an interface between the application programs and the computer. MS DOS (Microsoft’s Disk Operating System), UNIX are examples of system software.</p>
        <h4>Application software</h4>
        <p>Software that can perform a specific task for the user, such as word processing, accounting, budgeting or payroll, fall under the category of application software. Word processors,
        spreadsheets, database management systems are all examples of general purpose application software.</p>
        <h4>Types of application software are:</h4>
        <p><b>Word processing software:</b> The main purpose of this software is to produce documents. MS-Word, Word Pad, Notepad and some other text editors are some of the examples of word processing software.</p>
        <p><b>Database software:</b> Database is a collection of related data. The purpose of this software is to organize and manage data. The advantage of this software is that you can change the way data is stored and displayed. 
        MS access, dBase, FoxPro, Paradox, and Oracle are some of the examples of database software.</p>
        <p><b>Spread sheet software:</b> The spread sheet software is used to maintain budget, financial statements, grade sheets, and sales records. The purpose of this software is organizing numbers. It also allows the users to perform
        simple or complex calculations on the numbers entered in rows and columns. MS-Excel is one of the example of spreadsheet software.</p>
        <p><b>Presentation software:</b> This software is used to display the information in the form of slide show. The three main functions of presentation software is editing that allows insertion and formatting of text, including graphics in
        the text and executing the slide shows. The best example for this type of application software is Microsoft PowerPoint.</p>
        <p><b>Multimedia software:</b> Media players and real players are the examples of multimedia software. This software will allow the user to create audio and videos. The different forms of multimedia software are audio converters, players, burners, video encoders and decoders.</p>
