        <h3>Central Processing Unit(CPU) </h3>
        <p><b>Central Processing Unit(CPU):</b> is the main brain of the computer that accepts data, performs operations on the data and sends out the result.Information from an input device or from the computerÂ’s memory is communicated via the bus to the
        Central Processing Unit (CPU), which is the part of the computer that translates commands and runs programs. It consists of ALU and CU, and a single chip or series of chips that performs arithmetic and logical calculations and controls the operations of the other elements of the system.</p><br>
        <p><b>Most CPU chips are composed of four functional sections:</b>
        <p><b>ALU:</b> Calculating ability either arithmetical or logical operations. <br>
        <b>Registers:</b> Temporary storage areas that hold data, keep tracks of instruction, and hold the location and results of these operations. <br>
        <b>Control section:</b> Times and regulates the operation of the entire computer system, by using its instruction decoder to read patterns of data in a designated register and translate the patterns into activities, such as
        addition or comparison. It also uses its interrupt input to indicate the order in which individual operations uses the CPU and regulates the amount of CPU time allotted to each operation. <br>
        <b>Internal Bus:</b> Network of communication lines that connects the internal elements of the processor and also leads to external connectors that links the processor to the other element of the computer.</p>
        <p><b>The main functions of the microprocessor (CPU clips) includes the following.</b> <br>
    <span>a. Control use of the main storage in storing data and instructions (i.e the ROM). <br>
        b. Control the sequence of operations. <br>
        c. Give commands to all parts of the computer system. <br>
        d. Carry out processing.</span> </p>
        
